# CONVERT FLOAT TO INT

amount = int(10.6)
print(amount)  # 10

# CONVERT INT TO FLOAT

amount = float(20)
print(amount)  # 20.0

# STRING
name = 'Sarah'
print(name)

store_name = "Sarah's Store"
print(store_name)

# STRING CONCATENATION
hello = 'Hello'
name = 'Sarah'
greeting = hello + " " + name
print(greeting)

# CONSOLE INPUT
hello = 'Hello'
name = input('What is your name? \n') # \n print in new line
greeting = hello + " " + name
print(greeting)